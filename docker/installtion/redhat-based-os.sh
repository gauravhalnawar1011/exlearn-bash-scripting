#!/bin/bash

#################################################
# UPDATE PACKAGES
#################################################

sudo dnf update -y

#################################################
# INSTALL DOCKER
#################################################

sudo dnf install -y docker

#################################################
# START DOCKER
#################################################

sudo systemctl start docker

#################################################
# ENABLE DOCKER
#################################################

sudo systemctl enable docker

#################################################
# ADD EC2 USER TO DOCKER GROUP
#################################################

sudo usermod -aG docker ec2-user

#################################################
# VERIFY
#################################################

docker --version