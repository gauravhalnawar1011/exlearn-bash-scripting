#!/bin/bash

#################################################
# UPDATE PACKAGES
#################################################

sudo apt update -y

#################################################
# INSTALL DEPENDENCIES
#################################################

sudo apt install -y \
ca-certificates \
curl \
gnupg \
lsb-release

#################################################
# INSTALL DOCKER
#################################################

sudo apt install -y docker.io

#################################################
# START DOCKER
#################################################

sudo systemctl start docker

#################################################
# ENABLE DOCKER
#################################################

sudo systemctl enable docker

#################################################
# ADD USER TO DOCKER GROUP
#################################################

sudo usermod -aG docker ubuntu

#################################################
# VERIFY
#################################################

docker --version